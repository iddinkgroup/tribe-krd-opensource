/**
 * De rol die iemand bij een afspraak kan spelen.
 * 
 * @author loite
 */
public enum AfspraakRol
{
	/**
	 * Iemand die deelneemt aan de afspraak als een deelnemer (lerenden).
	 */
	Deelnemer,
	/**
	 * Een begeleider van de afspraak (de docent).
	 */
	Begeleider;

}
